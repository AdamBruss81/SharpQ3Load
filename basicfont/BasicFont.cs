		const float m_fCharWidth = 10;
		const float m_fCharHeight = 13;
		const float m_fYOrigOffset = 1;
		const float m_fLeftPadding = 1;

		int m_fontoffset;

		public BasicFont()
		{
			MakeRasterFont();
		}

		void MakeRasterFont() 
		{
			int i, j;
			Gl.glPixelStorei(Gl.GL_UNPACK_ALIGNMENT, 1);
			m_fontoffset = Gl.glGenLists(128);
			for (i = 0, j = ' '; i < 95; i++, j++)
			{
				Gl.glNewList(m_fontoffset + j, Gl.GL_COMPILE);
				Gl.glBitmap(8, (int)m_fCharHeight, 0.0f, m_fYOrigOffset, m_fCharWidth, 0.0f, rasters[i]);
				Gl.glEndList();
			}
		}

		public void Delete()
		{
			Gl.glDeleteLists(m_fontoffset, 128);
		}

		public void PrintString(string s) 
		{
			Gl.glPushAttrib(Gl.GL_LIST_BIT);
			Gl.glListBase(m_fontoffset);
			Gl.glCallLists(s.Length, Gl.GL_SHORT, s);
			Gl.glPopAttrib();
		}

		/// <summary>
		/// Break apart s by end line character and print each line on a new line
		/// </summary>
		/// <param name="s">value to write out</param>
		/// <param name="window">the opengl window</param>
		public void PrintTopLeft(string s, int windowWidth, int windowHeight, int nLineOffset)
		{
			string[] lines = s.Split(new Char[] { '\n' });

			for (int i = 0; i < lines.Length; i++)
			{
				Gl.glWindowPos2d(m_fLeftPadding, windowHeight - m_fCharHeight * (i + 1 + nLineOffset));
				PrintString(lines[i]);
			}
		}

		public void PrintTopCenter(string s, int windowWidth, int windowHeight, int nLineOffset)
		{
			string[] lines = s.Split(new Char[] { '\n' });

			for (int i = 0; i < lines.Length; i++)
			{
				Gl.glWindowPos2d(windowWidth / 2 - (lines[i].Length * (m_fCharWidth / 2)),
					windowHeight - m_fCharHeight * (i + 1 + nLineOffset));

				PrintString(lines[i]);
			}
		}

		public void PrintTopRight(string s, int windowWidth, int windowHeight, int nLineOffset)
		{
			string[] lines = s.Split(new Char[] { '\n' });

			for (int i = 0; i < lines.Length; i++)
			{
				Gl.glWindowPos2d(windowWidth - lines[i].Length * m_fCharWidth, 
					windowHeight - m_fCharHeight * (i + 1 + nLineOffset));

				PrintString(lines[i]);
			}
		}

		public void PrintCenter(string s, int windowWidth, int windowHeight, int nLineOffset) 
		{
			string[] lines = s.Split(new Char[] { '\n' });

			int nCounter = lines.Length - 1;
			for (int i = 0; i < lines.Length; i++)
			{
				Gl.glWindowPos2d((windowWidth / 2) - ((lines[i].Length * m_fCharWidth) / 2),
					(windowHeight / 2) + (m_fCharHeight / 2) + (m_fCharHeight * nCounter) + (nLineOffset * m_fCharHeight));

				PrintString(lines[i]);

				nCounter--;
			}
		}

		public void PrintLowerLeft(string s, int windowWidth, int nLineOffset)
		{
