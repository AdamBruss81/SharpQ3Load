		public void PrintGLUTCenter(string s, int windowWidth, int windowHeight, int nLineOffset)
		{
			string[] lines = s.Split(new Char[] { '\n' });

			int nCounter = lines.Length - 1;
			for (int i = 0; i < lines.Length; i++)
			{
				Gl.glWindowPos2d((windowWidth / 2) - ((lines[i].Length * m_fCharWidth-10) / 2),
					(windowHeight / 2) + (18 / 2) + (18 * nCounter) + (nLineOffset * 18));

				for (int j = 0; j < lines[i].Length; j++)
				{
					Glut.glutBitmapCharacter(Glut.GLUT_BITMAP_HELVETICA_18, (int)lines[i][j]);
				}

				nCounter--;
			}			
		}
